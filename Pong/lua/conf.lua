function love.conf(t)
    t.title = "Pong"                --título do jogo (string)
    t.version = "11.4"                  --versão de love que o jogo foi feito (string)
    t.console = true                    --console do windows (boolean, windows apenas)
    t.window.width = 1280               --largura da tela (number)
    t.window.heigh = 720                --altura da tela (number)
end